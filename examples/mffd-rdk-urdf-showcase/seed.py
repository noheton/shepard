def lookup_joint_channel_ids(host: str, api_key: str, container_id: int) -> dict[str, str]:
    """Return jointName → channel shepardId for the trajectory we just uploaded."""
    v2 = _v2_base(host)
    out: dict[str, str] = {}
    page = 0
    while True:
        resp = _http.get(
            f"{v2}/timeseries-containers/{container_id}/channels",
            params={"page": page, "size": 200},
            headers={"X-API-KEY": api_key, "Accept": "application/json"},
            timeout=30,
        )
        if resp.status_code != 200:
            break
        batch = resp.json()
        if not batch:
            break
        for ch in batch:
            if ch.get("device") != "KR210-R2700-2":
                continue
            field = ch.get("field")
            shepard_id = ch.get("shepardId")
            if field in JOINTS and shepard_id and field not in out:
                out[field] = shepard_id
        if len(batch) < 200:
            break
        page += 1
    return out


# ---------------------------------------------------------------------------
# Reset

def reset(apis: Apis) -> None:
    existing = _find_collection_by_name(apis, COLLECTION_NAME)
    if existing is None:
        _log("SKIP", COLLECTION_NAME, "Collection (no prior seed)")
    else:
        apis.collection.delete_collection(existing.id)
        _log("OK", COLLECTION_NAME, "Collection deleted", existing.id)
    for name, ctype, delete_fn in (
        (FC_RDK_NAME, ContainerType.FILE,       apis.file_container.delete_file_container),
        (TSC_NAME,    ContainerType.TIMESERIES, apis.timeseries_container.delete_timeseries_container),
    ):
        c = _find_container(apis, name, ctype)
        if c is None:
            _log("SKIP", name, f"{ctype} (no prior seed)")
            continue
        try:
            delete_fn(c.id)
            _log("OK", name, f"{ctype} deleted", c.id)
        except Exception as exc:
            _log("SKIP", name, f"delete failed: {str(exc)[:80]}")


