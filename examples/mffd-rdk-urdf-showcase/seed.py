# VIEW_RECIPE template (best-effort — requires instance-admin role)

def _find_view_recipe_template(host: str, api_key: str, name: str) -> str | None:
    v2 = _v2_base(host)
    try:
        resp = _http.get(f"{v2}/templates", params={"kind": TEMPLATE_KIND},
                         headers={"X-API-KEY": api_key, "Accept": "application/json"},
                         timeout=15)
        if resp.status_code != 200:
            return None
        rows = resp.json() if isinstance(resp.json(), list) else resp.json().get("content", [])
        for t in rows:
            if isinstance(t, dict) and t.get("name") == name:
                return t.get("appId")
    except Exception:
        return None
    return None


def ensure_view_recipe_template(host: str, api_key: str, joint_channel_ids: dict[str, str]) -> str | None:
    """POST a VIEW_RECIPE template wiring the KR210 URDF + joint→channel map.

    Returns the template appId or None if creation failed (typically 403 —
    the endpoint requires instance-admin and most demo users aren't).
    """
    existing = _find_view_recipe_template(host, api_key, TEMPLATE_NAME)
    if existing:
        _log("SKIP", TEMPLATE_NAME, "ShepardTemplate", existing)
        return existing

    body = {
        "renderer":         "urdf",
        "view":             "URDF + joint-trajectory animator",
        "shape":            "http://semantics.dlr.de/shepard-ui/urdf#UrdfViewShape",
        "viewRecipeShape":  "http://semantics.dlr.de/shepard-ui/urdf#UrdfViewShape",
        "urdfUrl":          URDF_PUBLIC_URL,
        "packagePath":      URDF_PACKAGE_DIR,
        "jointChannelMap":  joint_channel_ids,  # jointName → channel shepardId
        "label":            "MFFD AFP Ply 5 layup (KR210 R2700/2)",
    }
    payload = {
        "name":         TEMPLATE_NAME,
        "templateKind": TEMPLATE_KIND,
        "body":         json.dumps(body),
        "description":  ("Drives UrdfView with the KR210 R2700/2 KUKA URDF "
                         "+ the AFP joint trajectory's 6 joint-angle channels. "
                         "Channel binding via the urn:shepard:urdf:joint annotation."),
        "tags":         ["mffd", "urdf", "kr210_r2700_2", "view-recipe", "afp"],
    }
    v2 = _v2_base(host)
    resp = _http.post(f"{v2}/templates", json=payload,
                      headers={"X-API-KEY": api_key, "Content-Type": "application/json"},
                      timeout=30)
    if resp.status_code in (200, 201):
        app_id = resp.json().get("appId")
        _log("OK", TEMPLATE_NAME, "ShepardTemplate", app_id)
        return app_id
    if resp.status_code in (401, 403):
        _log("SKIP", TEMPLATE_NAME,
             "ShepardTemplate (needs instance-admin — view still launchable via URL)",
             resp.status_code)
        return None
    _log("WARN", TEMPLATE_NAME, "ShepardTemplate POST failed",
         f"{resp.status_code}: {resp.text[:120]}")
    return None


