# CLI

def main(argv: list[str] | None = None) -> None:
    ap = argparse.ArgumentParser(
        description="Idempotent seeder for the MFFD RDK→URDF showcase.",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog=(
            "Examples:\n"
            "  python3 seed.py --host https://shepard-api.nuclide.systems --apikey XXX\n"
            "  python3 seed.py --host http://localhost:8080 --apikey XXX --reset\n"
        ),
    )
    ap.add_argument("--host",   required=True, help="Shepard backend base URL")
    ap.add_argument("--apikey", required=True, help="API key with write access")
    ap.add_argument("--reset",  action="store_true", help="Delete prior collection + containers first")
    args = ap.parse_args(argv)

    cfg = Configuration(host=args.host, api_key={"apikey": args.apikey})
    client = ApiClient(configuration=cfg)
    apis = Apis(
        client=client,
        collection=CollectionApi(client),
        data_object=DataObjectApi(client),
        file_container=FileContainerApi(client),
        file_reference=FileReferenceApi(client),
        timeseries_container=TimeseriesContainerApi(client),
        timeseries_reference=TimeseriesReferenceApi(client),
        search=SearchApi(client),
    )

    if args.reset:
        print("=== RESET ===", flush=True)
        reset(apis)
        print(flush=True)

    seed(apis)


if __name__ == "__main__":
    main()
