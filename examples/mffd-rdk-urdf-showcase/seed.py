"""Idempotent seeder for the MFFD RDK→URDF showcase.

Bootstraps a Collection on a running Shepard backend demonstrating the
end-to-end RDK→URDF pipeline shipped by URDF-WEBVIEW-1 (phase 1) and
RDK-PARSE-1:

  1. The real DLR MFZ.rdk RoboDK station file (12.1 MB at
     ``../mffd-showcase/raw-data/mffd-data/cell/MFZ.rdk``) is uploaded
     as a FileReference. The RDK-PARSE-1 plugin scrapes tier-1
     ``urn:shepard:rdk:*`` annotations on upload (appVersion, platform,
     programSource, robotController, apiEndpoint, cadRef×3, stepRef×2).

  2. A KUKA KR210 R2700/2 URDF + its 7 binary STL meshes are uploaded as
     FileReferences for provenance. (The runtime fetch path is
     ``/urdf-samples/kr210_r2700_2/...`` from the frontend public dir — see
     ``urdf/SOURCES.md``.)

  3. A synthetic 30-second 6-joint trajectory (generated by
     ``trajectory/generate.py``) lands as a TimeseriesReference. Each
     channel is annotated with ``urn:shepard:urdf:joint = <jointName>``
     per the URDF-WEBVIEW-1 contract — the UrdfChannelPicker uses this
     annotation for auto-preselection (the channel→joint binding
     becomes one-click instead of manual).

  4. A VIEW_RECIPE :ShepardTemplate is best-effort created pointing at
     the KR210 URDF + joint-channel map. (Requires the instance-admin
     role — when running as a regular user, the template step is
     skipped with a clear log line; users can still launch the URDF
     view directly via the shapes/render URL printed at the end.)

Usage
-----
    python3 seed.py --host https://shepard-api.nuclide.systems --apikey XXX
    python3 seed.py --host http://localhost:8080 --apikey XXX --reset

The seeder is idempotent — re-running it detects existing entities by
name and skips work that's already done. ``--reset`` deletes the
showcase Collection and shared containers first.

Engineering context
-------------------
This showcase routes around the missing ``RdkToUrdfExporter`` sidecar
(phase 2, queued — RoboDK SDK licence required) by sourcing the URDF
from the open-source kroshu/kuka_robot_descriptions package
(Apache-2.0) — the **real** KR210 R2700/2 model used in the MFFD AFP
cell (per ``examples/mffd-showcase/seed.py`` line 114). See
``SHOWCASE.md`` for the honest caveats: tier-1 scrape is metadata-only
(no kinematics); the trajectory is synthetic but joint-limit-respecting
and plausible.
"""
