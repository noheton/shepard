CREATE TABLE spatial_data (
  id bigint generated by default as identity,
  container_id bigint not null,
  time bigint not null,
  geometry GEOMETRY,
  metadata JSONB,
  measurements JSONB
);

CREATE INDEX spatial_data_geometry_idx
  ON spatial_data
  USING GIST (geometry gist_geometry_ops_nd);

CREATE INDEX spatial_data_metadata_idx
  ON spatial_data
  USING GIN(metadata);
