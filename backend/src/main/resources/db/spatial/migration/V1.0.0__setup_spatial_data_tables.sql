CREATE TABLE spatial_data_points (
  id bigint generated by default as identity,
  container_id bigint not null,
  time bigint not null,
  position GEOMETRY not null,
  metadata JSONB,
  measurements JSONB
);

CREATE INDEX spatial_data_points_container_id_idx
  ON spatial_data_points(container_id);

CREATE INDEX spatial_data_point_position_idx
  ON spatial_data_points
  USING GIST (position gist_geometry_ops_nd);

CREATE INDEX spatial_data_points_metadata_idx
  ON spatial_data_points
  USING GIN(metadata);
